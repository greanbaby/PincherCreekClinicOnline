    <h1 class="western">
      Family and Community Support Services (FCSS)</h1>

      <p><strong>The Pincher Creek and District Family and Community
      Support Services</strong>&nbsp;facilitates preventative social
      services and community initiatives which contribute to quality of
      life and family wellness in the communities of the Town of Pincher
      Creek, the Municipal District of Pincher Creek #9 and the Village of
      Cowley.</p>

      <h3 class="western">What is FCSS?</h3>
      <p>Family &amp; Community Support Services (FCSS) is a joint
      municipal/provincial funding program established to develop, support,
      and fund preventive social services.<br/>
      <br/>
      FCSS is an 80/20
      funding partnership between the Government of Alberta, municipalities
      and Metis Settlements. Provincially,&nbsp;the FCSS Program receives
      its mandate from the <em>Family and Community Support Services
      Act</em>&nbsp;and<em>&nbsp;Regulation.</em></p>
      <p>Under FCSS, communities design and deliver social programs that
      are preventive in nature to promote and enhance well-being among
      individuals, families, and communities. The programs depend on
      community resources, often involving volunteers in management and
      delivery.</p>
      <p>The FCSS philosophy is based on a belief that self-help
      contributes to a sense of integrity, self-worth and independence. The
      developed programs are intended to help individuals in their
      community to adopt healthy lifestyles, thereby improving the quality
      of life and building the capacity to prevent and/or deal with crisis
      situations should they arise.</p>
      <p>One of the key principles of the FCSS Program is
      local&nbsp;responsibility in priority-setting and resource
      allocation. FCSS emphasizes local decision-making for programming,
      based on the belief that communities have the desire, energy and
      resources to build community capacity. Each municipality or Metis
      Settlement, therefore,&nbsp;determines how the dollars they receive
      should be allocated to best meet local&nbsp;needs.</p>

      <h3 class="western">Who benefits from FCSS?</h3>
      <p>Programs funded by FCSS provide preventive services to all age
      groups and people from all walks of life, such as family counselling
      for persons in distress, care for isolated seniors, and support for
      children and youth.</p>

      <h3 class="western">Why focus on prevention?</h3>
      <p>Research shows that an investment of $1 in preventive services
      yields a $7 return in avoided costs such as police, justice,
      additional treatment and increased productivity in employment and
      contribution to society.</p>

      <h3 class="western">Who manages FCSS programs?</h3>
      <p>The Town of Pincher Creek FCSS manages the FCSS funding programs
      for the Town, the Municipal District of Pincher Creek #9, and the
      Village of Cowley. The Town also collaborates with agencies and the
      community to better understand the magnitude of social issues and how
      best to respond to them.</p>
      <p>The FCSS Board of Directors, with representation from the Town of
      Pincher Creek, Municipal District of Pincher Creek, Cowley, and four
      members at large from the community, reviews funding requests from
      social agencies and selects projects for funding.</p>

      <h3 class="western">What types of programs and services will FCSS
      fund?</h3>
      <p>In addition to being consistent with the FCSS Act, programs and
      services must:</p>
      <ul>
        <li><p style="margin-bottom: 0in">Be consistent with the funding
        priorities of the Town and Municipal District of Pincher Creek and
        the Village of Cowley 
        </p></li>
        <li><p style="margin-bottom: 0in">Be collaborative and not duplicate
        existing programs 
        </p></li>
        <li><p style="margin-bottom: 0in">Involve volunteers 
        </p></li>
        <li><p style="margin-bottom: 0in">Leverage additional funds to
        assist with program costs 
        </p></li>
        <li><p style="margin-bottom: 0in">State objectives in measurable
        terms 
        </p></li>
        <li><p style="margin-bottom: 0in">Demonstrate the capacity to
        achieve stated outcomes 
        </p></li>
        <li><p>Demonstrate good governance and administration 
        </p></li>
      </ul>

